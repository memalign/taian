
function switchLang(newLang) {
    var currLang = getPageAttribute("l");
    if (currLang == newLang)
        return;

    updateHash("l", newLang);
    document.location.reload(true)
}

function langIsChinese() {
    var lang = getPageAttribute("l");
    return (lang == "cn");
}

function langTag() {
    if (langIsChinese()) {
        return "cn";
    }
    return "en";
}

